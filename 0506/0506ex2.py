str="once" # 문자열 자료형
str_list=['t','w','i','c','e' ] # 리스트 자료형
num_tup=(5,3,2,1,4) # 튜플 자료형
num1=[1,2,3]
num2=[4,5,6]
print(str[3]) #인덱싱-str변수의 인덱스 3 위치 원소 추출
print(str_list[2:]) #슬라이싱 -인덱스 2~끝까지 추출
print(num_tup[:3]) # 슬라이싱-처음부터 인덱스 2까지 추출

print('str변수의 길이는 {}이다'.format(len(str))) # str함수의 길이를 구하는 코드
print('문자 "s"는 str_list에 있는 원소인가?  {}'.format('s' in str_list)) # str_list에 s가 있는지 없는지 알수있는 코드
print('once'*3) # 문자열 "once" 3번 반복
